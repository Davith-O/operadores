# Ejemplo operadores de comparacion
m = int(input("Ingrese el valor de m: "))
n = int(input("Ingrese el valor de n: "))
print("La igualdad es: ",m == n) #Igualdad
print("M es mayor que N? ",m > n)
print("M es menor que N? ",m<n)
print("M es mayor o igual a N? ",m>=n)
print("M es menor o igual a N?",m<=n)
print("M es diferente de N? ",m!=n)