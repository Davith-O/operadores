#Ejemplos con input
numero3 = int(input("Ingrese el numero 1: "))
numero4 = int(input("Ingrese el numero 2: "))

suma1 = numero3 + numero4
print("La suma es: ",suma1)

#operaciones aritmeticas
resta = numero3 - numero4
multiplicacion = numero3 * numero4
division = numero3 / numero4
modulo = numero3 % numero4
divisionEntera = numero3 // numero4
print(f"La resta es {numero3} y {numero4} es: {resta}")
print(f"La multiplicacion es {numero3} y {numero4} es: {multiplicacion}")
print(f"La division es {numero3} y {numero4} es: {division}")
print(f"La modulo es {numero3} y {numero4} es: {modulo}")
print(f"La division entera es {numero3} y {numero4} es: {divisionEntera}")

