# Ejemplo de operadores de asignacion
var1 = 5
var2 = 12
var3 = 8
var4 = 14
var5 = 6
var6 = 8

var1 += 3 # var1 = var1 + 3 Asignacion con suma
print(f"Asignacion con suma: {var1}")
var2 -= 3
print(f"Asignacion con resta: {var2}")
var3 *= 3
print(f"Asignacion con multiplicacion: {var3}")
var4 /= 2
print(f"Asignacion con division: {var4}")
var5 %= 3
print(f"Asignacion con modulo: {var5}")
var6 //= 3
print(f"Asignacion con division entera: {var6}")